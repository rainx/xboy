Disassembler::Disassembler(std::shared_ptr<mmu::Mmunit> mmu) : mmu_(mmu) {}

DisasmLine Disassembler::disassemble(uint16_t addr) const {
  uint8_t opcode = mmu_->get(addr);

  if (opcode == 0xCB) {
    uint8_t cb_op = mmu_->get(addr + 1);
    const auto &info = CB_OPCODES[cb_op];
    return {addr, info.fmt, 2};
  }

  const auto &info = BASE_OPCODES[opcode];
  char buf[64];

  switch (info.operands) {
  case 0:
    std::snprintf(buf, sizeof(buf), "%s", info.fmt);
    break;
  case 1: {
    uint8_t op1 = mmu_->get(addr + 1);
    std::snprintf(buf, sizeof(buf), info.fmt, op1);
    break;
  }
  case 2: {
    uint16_t op16 = mmu_->get(addr + 1) |
                    (static_cast<uint16_t>(mmu_->get(addr + 2)) << 8);
    std::snprintf(buf, sizeof(buf), info.fmt, op16);
    break;
  }
  }

  return {addr, buf, static_cast<uint8_t>(1 + info.operands)};
}

std::vector<DisasmLine> Disassembler::disassembleN(uint16_t addr, int n) const {
  std::vector<DisasmLine> lines;
  lines.reserve(n);
  for (int i = 0; i < n; i++) {
    auto line = disassemble(addr);
    addr += line.bytes;
    lines.push_back(std::move(line));
  }
  return lines;
}

